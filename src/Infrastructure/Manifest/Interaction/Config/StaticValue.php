namespace SprykerSdk\Sdk\Infrastructure\Manifest\Interaction\Config;

class StaticValue implements InteractionValueConfig
{
    /**
     * @var mixed
     */
    protected $value;

    /**
     * @param mixed $value
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * @return mixed
     */
    public function getValue()
    {
        return $this->value;
    }
}
