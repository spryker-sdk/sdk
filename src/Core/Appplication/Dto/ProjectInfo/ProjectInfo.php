namespace SprykerSdk\Sdk\Core\Appplication\Dto\ProjectInfo;

class ProjectInfo
{
    /**
     * @var string
     */
    protected string $name;

    /**
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
